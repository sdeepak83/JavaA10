package Part1.Lecture.Operator;

public class Relatonal {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }

}
